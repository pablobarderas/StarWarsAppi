    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if("".equals(txtName.getText())){
            JOptionPane.showMessageDialog(null, "Introduce un nombre");
        }else{
            String name = txtName.getText();
            
            
            StarWarsCharacter character = new StarWarsCharacter(name);
          
            // Show Character on List
            model.removeAllElements();
            modelResults.setModel(model);
            
            character.getResults().stream().parallel()
                    .forEach(person -> model.addElement(person.getName()
                    + " - PLANETA: "+person.getHomeworld()
                    + " - GÃ‰NERO: "+person.getGender()
                    + " - OJOS: "+person.getEye_color()
                    + " - PELO: "+person.getHair_color()));
            
            // Total results
            txtTotalResults.setText(String.valueOf(character.getSizeResults()));
            
        }
    }//GEN-LAST:event_btnSearchActionPerformed

